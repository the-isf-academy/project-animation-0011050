from turtle import *
pensize(20)
speed(1000)
def body(color,size):
    fillcolor(color)
    begin_fill()
    right(90)
    forward(50*size)
    right(180)
    circle(40*size, -180)
    right(180)
    forward(200*size)
    right(180)
    circle(110*size,-180)
    right(180)
    forward(200*size)
    right(180)
    circle(40*size,-180)
    right(180)
    forward(50*size)
    right(90)
    left(30*size)
    circle(60,-50*size)
    end_fill()
def goggles(color,size):
    right(180)
    circle(40,180)
    setheading(0)
    forward(60)
    circle(40,180)
    forward(60)
    end_fill()

def draw_figure(color,size):
    fillcolor("gray")
    begin_fill()
    body(color,size)
    setheading(90)
    penup()
    forward(200*size)
    right(90)
    forward(80)
    pendown()



draw_figure("blue",1)
